# Calcular la distancia recorrida en ciertos tiempos y cuanto tardará en recorrer una distancia.

v=float(input("Dame la velocidad de tu coche en km/h "))
d=v*6
print("Esta es la distancia recorrida en 6hrs: ",d)
t=v*3.5
print("Esta es la distancia recorrida en 3.5hrs: ",t)
h=485/v
print("Estas son las horas que te tardas en recorrer 485km: ",round(h,2))


